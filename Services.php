<h1>Depuis plus de vingt ans, l’OIF déploie des efforts en faveur de la réduction des inégalités de genre dans ses différents domaines d’intervention au sein de l’espace francophone.</h1>
<p>Il y a aujourd’hui près de 140 millions de femmes vivant dans les pays francophones ; elles seront plus de 350 millions en 2050. Malgré de nombreuses avancées au niveau législatif ou dans le domaine de l’éducation, un ralentissement des progrès en faveur des droits des femmes et des filles, et parfois même leur remise en cause, est à noter. L’OIF œuvre ainsi pour préserver les acquis et éliminer les défis persistants de la mise en œuvre des engagements nationaux et internationaux en faveur de l’égalité femmes-hommes, des droits et de l’autonomisation des femmes et des filles.</p>
